class Supplier:
    def __init__(
        self,
        supplier_id=None,
        supplier_name=None,
        contact_person=None,
        contact_number=None,
        email=None,
    ):
        self.supplier_id = supplier_id
        self.supplier_name = supplier_name
        self.contact_person = contact_person
        self.contact_number = contact_number
        self.email = email
